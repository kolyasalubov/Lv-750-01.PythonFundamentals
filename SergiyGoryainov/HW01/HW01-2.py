a = int(input("Please enter a whole number (A): "))
b = int(input("Please enter another whole number (B): "))

sum = a + b
difference = a - b
product = a * b
quotient = a / b
exponent = a ** b
floor = a // b
remainder = a % b

print(f"A + B = {sum}")
print(f"A - B = {difference}")
print(f"A * B = {product}")
print(f"A / B = {quotient}")
print(f"A ** B = {exponent}")
print(f"A // B = {floor}")
print(f"A % B = {remainder}")
